// Q12. Write a program that demonstrates all 6 primitive data types in JavaScript with real-world examples for
// each.

// String
let userName="Rohit Sharma"
console.log(`User Name Is : ${userName}`)

// Number
let age=20
console.log(`Age Is : ${age}`)

// Boolean
let isLoggedIn = true
if(isLoggedIn){
    console.log(`User logged in !`)
}

// Symbol
let id=Symbol("id")
console.log(`Id is : ${id}`)

// null
let email=null
console.log(`${email}`)

// undefined
let mobileNumber=undefined
console.log(`${mobileNumber}`)